    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        String Nome;
        int Codigo;
        
        
        if(jTableInsumo.getSelectedRow() != -1){
            Nome = jTableInsumo.getValueAt(jTableInsumo.getSelectedRow(), 0).toString();
            Codigo = objInsumo.PesquisarNome(Nome);
            if(objInsumo.Excluir(Nome)){
                objInsumo.carregaTabela(jTableInsumo);
            }else{
                int opcao = JOptionPane.showConfirmDialog(null, Nome+" possui informações associadas a ele! Deseja"
                        + " apagar tudo?\nEsta operação não podera ser desfeita!", "Excluir Insumo", JOptionPane.YES_NO_OPTION);
                
                if(opcao == 0){
                    
                    objInsumo.ExcluirEntrada(Codigo);
                    objInsumo.ExcluirSaida(Codigo);
                    objInsumo.ExcluirPedido(Codigo);
                    objInsumo.Excluir(Nome);
                    objInsumo.carregaTabela(jTableInsumo);
                }else{
                    
                }
                
            }
            
            
            
        }else{
            JOptionPane.showMessageDialog(this,"Selecione uma linha!!!");
        }
    }//GEN-LAST:event_ExcluirActionPerformed

    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed
        if(jTableInsumo.getSelectedRow() != -1){
            Nome = jTableInsumo.getValueAt(jTableInsumo.getSelectedRow(), 0).toString();
        
            JDialogCadInsumo Dialog1 = new JDialogCadInsumo(new javax.swing.JDialog(), true);
            Dialog1.Alterar(Nome);
            Dialog1.setVisible(true);
            objInsumo.carregaTabela(jTableInsumo);
            
        }else{
            JOptionPane.showMessageDialog(this,"Selecione uma linha!!!");
        }
    }//GEN-LAST:event_AlterarActionPerformed

