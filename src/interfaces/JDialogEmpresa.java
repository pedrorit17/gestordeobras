    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed
        if(jTableEmpresa.getSelectedRow() != -1){
            Nome = jTableEmpresa.getValueAt(jTableEmpresa.getSelectedRow(), 0).toString();

            JDialogCadEmpresa frmDialog2 = new JDialogCadEmpresa(new javax.swing.JDialog(), true);
            frmDialog2.Alterar(Nome);
            frmDialog2.setVisible(true);
            objEmpresa.carregaTabela(jTableEmpresa);
        }else{
            JOptionPane.showMessageDialog(this,"Selecione uma linha!!!");
        }
        
    }//GEN-LAST:event_AlterarActionPerformed

