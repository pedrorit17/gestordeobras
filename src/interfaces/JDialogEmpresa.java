    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        String Nome;
        int Codigo;
        
        
        if(jTableEmpresa.getSelectedRow() != -1){
            Nome = jTableEmpresa.getValueAt(jTableEmpresa.getSelectedRow(), 0).toString();
            Codigo = objEmpresa.PesquisaridEmpresa(Nome);
            objEmpresa.Excluir(Nome);
            objEndereco.Excluir(Codigo);
            objEmpresa.carregaTabela(jTableEmpresa);
            if(objEmpresa.PesquisarExite() >= 1){
            Novo.setEnabled(false);
            }else{
             Novo.setEnabled(true);   
            }
        }else{
            JOptionPane.showMessageDialog(this,"Selecione uma linha!!!");
        }
    }//GEN-LAST:event_ExcluirActionPerformed

