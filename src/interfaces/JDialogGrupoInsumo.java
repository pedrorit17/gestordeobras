    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        String Nome;
        int Codigo;

        if (jTableGInsumo.getSelectedRow() != -1) {
            Nome = jTableGInsumo.getValueAt(jTableGInsumo.getSelectedRow(), 0).toString();
            Codigo = objGrupoInsumo.Pesquisarid(Nome);
            if (Codigo == -1) {
                objGrupoInsumo.Excluir(Nome);
                objGrupoInsumo.carregaTabela(jTableGInsumo);

            } else {
                int opcao = JOptionPane.showConfirmDialog(null, Nome + " possui informações associadas a ele! Deseja"
                        + " apagar tudo?\nEsta operação não podera ser desfeita!", "Excluir Grupo Insumo", JOptionPane.YES_NO_OPTION);

                if (opcao == 0) {

                    ConsultaSQL = "SELECT i.idInsumo FROM grupoinsumo g, insumo i WHERE g.Nome = ? and g.idGrupoInsumo = i.GrupoInsumo_idGrupoInsumo";

                    try {
                        PreparedStatement Query1 = c.prepareStatement(ConsultaSQL);
                        Query1.setString(1, Nome);
                        rs = Query1.executeQuery();

                        while (rs.next()) {// se quisesse mais de uma colocar um while
                            objGrupoInsumo.ExcluirEntrada(rs.getInt("i.idInsumo"));
                            objGrupoInsumo.ExcluirSaida(rs.getInt("i.idInsumo"));
                            objGrupoInsumo.ExcluirPedido(rs.getInt("i.idInsumo"));
                            objGrupoInsumo.ExcluirInsumo(rs.getInt("i.idInsumo"));

                        }
                    } catch (java.sql.SQLException e) {
                        System.out.println(e.getMessage());
                    }

                    objGrupoInsumo.Excluir(Nome);
                    objGrupoInsumo.carregaTabela(jTableGInsumo);
                } else {

                }
            }
        }
    }//GEN-LAST:event_ExcluirActionPerformed

