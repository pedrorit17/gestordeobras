    private void NovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NovoActionPerformed
        if (Nome.getText().equals("") || Sigla.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campos com * são obrigatorios!!!");
        } else {
            objGrupoInsumo.setNome(Nome.getText());
            objGrupoInsumo.setSigla(Sigla.getText());

            if (objGrupoInsumo.PesquisarNome(Nome.getText()) == -1) {
                if (objGrupoInsumo.Salvar()) {
                    JOptionPane.showMessageDialog(this, "Dados gravados com sucesso!!!");
                    objGrupoInsumo.carregaTabela(jTableGInsumo);
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao salvar dados!!!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Nome de Grupo Insumo já existente favor mudar!!!");
            }
        }
    }//GEN-LAST:event_NovoActionPerformed

