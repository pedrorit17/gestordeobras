    private void ConfEntregaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfEntregaActionPerformed
        String NomeF, NomeI, DataE;
        int CodF, CodI;
        Double Qtd, Total;

        if (jTableAtrasados.getSelectedRow() != -1) {
            NomeF = jTableAtrasados.getValueAt(jTableAtrasados.getSelectedRow(), 0).toString();
            DataE = jTableAtrasados.getValueAt(jTableAtrasados.getSelectedRow(), 1).toString();
            NomeI = jTableAtrasados.getValueAt(jTableAtrasados.getSelectedRow(), 2).toString();
            Qtd = Double.parseDouble(jTableAtrasados.getValueAt(jTableAtrasados.getSelectedRow(), 3).toString());

            CodF = objPedidoCompras.PesquisaridFornecedor(NomeF);

            CodI = objPedidoCompras.PesquisaridInsumo(NomeI);

            format.setLenient(false);
            try {
                data = format.parse(DataE);

            } catch (ParseException ex) {
                ex.printStackTrace();
            }

            dataUtil = data;

            java.sql.Date dataSql = new java.sql.Date(dataUtil.getTime());

            if (objPedidoCompras.Alterar(CodF, dataSql, CodI, Qtd)) {
                Total = objInsumo.PesquisarQtdInsumo(CodI);
                Total = Qtd + Total;
                objInsumo.AlteraQuantidade(CodI, Total);
            }

            objPedidoCompras.carregaTabelaInicio(jTableAtrasados);

        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma linha!!!");
        }
    }//GEN-LAST:event_ConfEntregaActionPerformed

