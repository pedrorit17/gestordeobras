    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed

        if (jTableAtrasados.getSelectedRow() != -1) {
            int CodF = objPedidoCompras.PesquisaridFornecedor(jTableAtrasados.getValueAt(jTableAtrasados.getSelectedRow(), 0).toString());

            int CodI = objPedidoCompras.PesquisaridInsumo(jTableAtrasados.getValueAt(jTableAtrasados.getSelectedRow(), 2).toString());

            String Quantidade = jTableAtrasados.getValueAt(jTableAtrasados.getSelectedRow(), 3).toString();

            String Data = jTableAtrasados.getValueAt(jTableAtrasados.getSelectedRow(), 1).toString();

            Double Qtd;

            Qtd = Double.parseDouble(Quantidade);

            objPedidoCompras.setCodFornecedor(CodF);
            objPedidoCompras.setCodInsumo(CodI);
            objPedidoCompras.setQuantidade(Qtd);

            dataTexto = Data;

            format.setLenient(false);
            try {
                data = format.parse(dataTexto);
            } catch (ParseException ex) {
                ex.printStackTrace();
            }

            dataUtil = data;

            java.sql.Date dataSql = new java.sql.Date(dataUtil.getTime());

            objPedidoCompras.setDataEntrega(dataSql);

            if (objPedidoCompras.Excluir()) {
                objPedidoCompras.carregaTabelaInicio(jTableAtrasados);
            } else {

            }

        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma linha!!!");
        }


    }//GEN-LAST:event_ExcluirActionPerformed

