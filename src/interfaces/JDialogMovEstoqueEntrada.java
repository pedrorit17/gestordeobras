    private void jtfPesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfPesquisaKeyPressed
        String consultaSQL = "SELECT Nome FROM insumo WHERE Nome LIKE ? ORDER BY Nome";
        PreparedStatement query;
        ResultSet rs;
        String pesquisa = jtfPesquisa.getText();
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            Insumo.removeAllItems();

            try {
                query = c.prepareStatement(consultaSQL);
                query.setString(1, "%" + pesquisa + "%");
                rs = query.executeQuery();

                while (rs.next()) {
                    Insumo.addItem(rs.getString("Nome"));
                }

            } catch (java.sql.SQLException e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_jtfPesquisaKeyPressed

