    private void jComboBoxGIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxGIActionPerformed
        if (jComboBoxGI.getSelectedItem().equals("Todos")) {
            Insumo.removeAllItems();
            ConsultaSQL = "SELECT Nome FROM insumo ORDER BY Nome";

            try {
                PreparedStatement Query1 = c.prepareStatement(ConsultaSQL);
                rs = Query1.executeQuery();

                while (rs.next()) {// se quisesse mais de uma colocar um while

                    Insumo.addItem(rs.getString("Nome"));

                }
            } catch (java.sql.SQLException e) {
                System.out.println(e.getMessage());
            }
        } else {
            Insumo.removeAllItems();

            int CodGI;

            CodGI = objEntradaInsumo.PesquisaridGrupoInsumo((String) jComboBoxGI.getSelectedItem());

            Connection c = ConectaBD.getconexao();
            String ConsultaSQL = "";
            ConsultaSQL = "SELECT Nome FROM insumo WHERE GrupoInsumo_idGrupoInsumo = ? ORDER BY Nome";
            ResultSet rs;

            try {
                PreparedStatement Query1 = c.prepareStatement(ConsultaSQL);
                Query1.setInt(1, CodGI);
                rs = Query1.executeQuery();

                while (rs.next()) {// se quisesse mais de uma colocar um while

                    Insumo.addItem(rs.getString("Nome"));

                }
            } catch (java.sql.SQLException e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_jComboBoxGIActionPerformed

