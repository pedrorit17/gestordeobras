    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
        if(Nome.getText().equals("") || Cpf.getText().equals("") || Telefone.getText().equals("") ||
                FuncaoCargo.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Campos com * são obrigatorios!!!");
        }else{
            objFuncionario.setNome(Nome.getText());
            objFuncionario.setCPF(Cpf.getText());
            objFuncionario.setTelefone(Telefone.getText());
            objFuncionario.setFuncaoCargo(FuncaoCargo.getText());
            objFuncionario.setCelular(Celular.getText());
            
            if(objFuncionario.PesquisarCPF(Cpf.getText()) == -1){
                if(objFuncionario.Salvar()){
                    JOptionPane.showMessageDialog(this, "Dados gravados com sucesso!!!");
                    Nome.setText("");
                    Cpf.setText("");
                    Telefone.setText("");
                    FuncaoCargo.setText("");
                    Celular.setText("");
                }else{
                    JOptionPane.showMessageDialog(this, "Erro ao salvar dados!!!");
                }
            }else{
                JOptionPane.showMessageDialog(this, "O CPF já existe favor cadastrar um novo!!!");
            }
        }
    }//GEN-LAST:event_SalvarActionPerformed

