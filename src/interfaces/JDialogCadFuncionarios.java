    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed
        if(FuncaoCargo.getText().equals("") ||
                 Telefone.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Campo com * são obrigatorios!!!");
            
        }else{
            
            objFuncionario.setTelefone(Telefone.getText());
            objFuncionario.setCelular(Celular.getText());
            objFuncionario.setFuncaoCargo(FuncaoCargo.getText());
            
            if(objFuncionario.Alterar(CPFJDialog)){
                    JOptionPane.showMessageDialog(this,"Alterados com sucesso!!!");
                    dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this,"Erro ao alterar dados!!!");
                }

            }
    }//GEN-LAST:event_AlterarActionPerformed

