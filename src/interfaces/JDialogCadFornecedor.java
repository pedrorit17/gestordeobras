    private void SalvarVoltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarVoltarActionPerformed
        String opcao = Tipo.getSelection().getActionCommand();
        
        if(Rua.getText().equals("") || Bairro.getText().equals("") || Numero.getText().equals("") 
                || Cidade.getText().equals("") || Estado.getText().equals("") || CEP.getText().equals("") ||
                Nome.getText().equals("") || CPFCNPJ.getText().equals("") || Telefone.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Campo com * são obrigatorios!!!");
            
        }else{
            objEndereco.setRua(Rua.getText());
            objEndereco.setNumero(Numero.getText());
            objEndereco.setBairro(Bairro.getText());
            objEndereco.setCidade(Cidade.getText());
            objEndereco.setEstado(Estado.getText());
            objEndereco.setCEP(CEP.getText());
            objFornecedor.setNome(Nome.getText());
            objFornecedor.setCNPJ(CPFCNPJ.getText());
            objFornecedor.setTelefone(Telefone.getText());
            if("PessoaFisica".equals(opcao)){
                objFornecedor.setTipo(1);// pessoa fisica
            }else{
                objFornecedor.setTipo(2);// pessoa jurida
            }
            objFornecedor.setCelular(Celular.getText());
           
            if(DataNasc.getText().equals("  /  /    ")){
                dataTexto = "01/01/2000";
         
                format.setLenient(false);
                try {
                    data = format.parse(dataTexto);
                } catch (ParseException ex) {
                    ex.printStackTrace();
                }

                dataUtil = data;

                java.sql.Date dataSql = new java.sql.Date(dataUtil.getTime());

                objFornecedor.setDataNasc(dataSql);
            }else{
                String dataTexto = new String(DataNasc.getText());
         
                format.setLenient(false);
                try {
                    data = format.parse(dataTexto);
                } catch (ParseException ex) {
                    ex.printStackTrace();
                }

                dataUtil = data;

                java.sql.Date dataSql = new java.sql.Date(dataUtil.getTime());

                objFornecedor.setDataNasc(dataSql);
            }
         
           
            objFornecedor.setEmail(Email.getText());
            objFornecedor.setRG(RG.getText());
            
            if(objFornecedor.PesquisarNome(Nome.getText()) == -1){
                if(objEndereco.Salvar()){
                    JOptionPane.showMessageDialog(this,"Dados gravados com sucesso!!!");
                }
                else{
                    JOptionPane.showMessageDialog(this,"Erro ao salvar dados!!!");
                }

                objFornecedor.setCod_End(objEndereco.PesquisaridEndereco());

                if(objFornecedor.Salvar()){
                    dispose();
                }
                else{
                }
            }else {
                JOptionPane.showMessageDialog(this,"Nome de Fornecedor já existente favor mudar!!!");
            }
        }
    }//GEN-LAST:event_SalvarVoltarActionPerformed

