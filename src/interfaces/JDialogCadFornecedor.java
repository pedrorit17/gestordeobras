    public void JDialogCadFornecedor(String Nome1) {
        NomeJDialog = Nome1;
        
        Salvar.setEnabled(false);
        SalvarVoltar.setEnabled(false);
        Alterar.setEnabled(true);
        Nome.setEnabled(false);
        DataNasc.setEnabled(false);
        CPFCNPJ.setEnabled(false);
        RG.setEnabled(false);
        PessoaFisica.setEnabled(false);
        PessoaJuridica.setEnabled(false);
        
        
        
        
        if(NomeJDialog != null){
            
            objFornecedor.Pesquisar(NomeJDialog);
            
            
            codEnd = objFornecedor.getCod_End();
            
            objEndereco.Pesquisar(codEnd);
            
            DataNasc.setText(format.format(objFornecedor.getDataNasc()));
            
            Nome.setText(objFornecedor.getNome());
            
            CPFCNPJ.setText(objFornecedor.getCNPJ());
            Telefone.setText(objFornecedor.getTelefone());
            Celular.setText(objFornecedor.getCelular());
            Email.setText(objFornecedor.getEmail());
            RG.setText(objFornecedor.getRG());
            if(objFornecedor.getTipo() == 1){
                PessoaFisica.setSelected(true);
                PessoaJuridica.setSelected(false);
            }else{
                PessoaJuridica.setSelected(true);
                PessoaFisica.setSelected(false);
            }
            
            Rua.setText(objEndereco.getRua());
            Numero.setText(objEndereco.getNumero());
            Bairro.setText(objEndereco.getBairro());
            Cidade.setText(objEndereco.getCidade());
            Estado.setText(objEndereco.getEstado());
            CEP.setText(objEndereco.getCEP());
                       
            
        }
    }

    

